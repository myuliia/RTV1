#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*src;
	size_t	i;

	i = 0;
	src = s;
	if (!n)
		return ;
	while (i < n)
	{
		src[i] = '\0';
		i++;
	}
}
